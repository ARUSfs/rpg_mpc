    np.array(k)
    dx=np.array(x)-msg.x
    dy=np.array(y)-msg.y
    dist = [np.linalg.norm([dx[i],dy[i]]) for i in range(len(x))]
    i=np.argmin(dist)
    d_min=np.min(dist)
    s[i]

    theta = msg.yaw - np.arctan2(y[(i+5)%len(x)]-y[i],x[(i+5)%len(x)]-x[i])
    # rospy.loginfo([s[i],d_min,theta])
    
    k_msg = Float32MultiArray()
    k_msg.data=k
    k_pub.publish(k_msg)

    s_msg = Float32MultiArray()
    s_msg.data=s
    s_pub.publish(s_msg)

    x_msg = Float32MultiArray()
    x_msg.data = [s[i],d_min,theta,msg.vx,msg.vy,msg.r,par,delta]
    x_pub.publish(x_msg)

